///**Dado que a fórmula para conversão de Fahrenheit para Celsius é C = 5/9 (F – 32), ler um valor de temperatura em Fahrenheit e exibi-lo em Celsius */

let fahrenheit = Number(prompt('Informe por gentileza a temperatura em graus Fahrenheit:'))

let Celsius = 9/5 * (fahrenheit - 32)

alert('O valor da temperatura em Celsius é ' + Celsius + 'ºC')

